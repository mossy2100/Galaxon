namespace Galaxon.Core.Strings;

/// <summary>
/// Represents a list of bracket types for various use cases.
/// </summary>
public enum EBracketsType
{
    /// <summary>
    /// Round brackets or parentheses: ( and )
    /// </summary>
    Round,

    /// <summary>
    /// Square brackets: [ and ]
    /// </summary>
    Square,

    /// <summary>
    /// Curly brackets or braces: { and }
    /// </summary>
    Curly,

    /// <summary>
    /// Angle brackets: &lt; and &gt;
    /// </summary>
    Angle
}
